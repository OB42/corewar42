#include "vm.h"

void		ft_print_map(char *arena)
{
	int		i;
	int		x;

	x = 0;
	i = 0;
	while (i < (4*MEM_SIZE))
	{
		x = 0;
		while (x < 4)
		{
			printf("%c", arena[i]);
			i++;
			x++;
		}
		printf("-");
	}
}
